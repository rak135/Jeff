"""Family-aware builders for direct research archive artifacts."""

from __future__ import annotations

from .ids import allocate_archive_artifact_id
from .models import (
    ArchiveEvidenceItem,
    ClaimEvidenceLink,
    ResearchArchiveArtifact,
    SourceGrouping,
    make_archive_artifact,
)


def create_research_brief(
    *,
    project_id: str,
    title: str,
    summary: str,
    question_or_objective: str,
    findings: tuple[str, ...],
    inference: tuple[str, ...],
    uncertainty: tuple[str, ...],
    source_refs: tuple[str, ...],
    work_unit_id: str | None = None,
    run_id: str | None = None,
    staleness_sensitivity: str = "medium",
    derived_from_artifact_ids: tuple[str, ...] = (),
    artifact_id: str | None = None,
    generated_at: str | None = None,
) -> ResearchArchiveArtifact:
    return make_archive_artifact(
        artifact_id=artifact_id or str(allocate_archive_artifact_id()),
        artifact_family="research_brief",
        project_id=project_id,
        work_unit_id=work_unit_id,
        run_id=run_id,
        title=title,
        summary=summary,
        question_or_objective=question_or_objective,
        findings=findings,
        inference=inference,
        uncertainty=uncertainty,
        source_refs=source_refs,
        generated_at=generated_at,
        staleness_sensitivity=staleness_sensitivity,
        derived_from_artifact_ids=derived_from_artifact_ids,
    )


